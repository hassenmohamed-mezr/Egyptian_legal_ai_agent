# 4. BUILD CONTEXT
# ============================================================
def build_legal_context(
    chunks: Sequence[Dict[str, Any]],
    max_chars: int = 2500,
) -> str:

    if not chunks:
        return "لا توجد نصوص."

    chunks = sorted(chunks, key=lambda x: x.get("score", 0), reverse=True)

    parts = []
    total = 0

    for c in chunks[:5]:
        score = c.get("score", 0)
        if score < 0.3:
            continue

        text       = c.get("text", "").strip()
        article_id = c.get("article_id", "?")

        block = f"المادة ({article_id}):\n{text}\n\n"

        if total + len(block) > max_chars:
            break

        parts.append(block)
        total += len(block)

    return "\n".join(parts) if parts else "لا يوجد نصوص كافية."